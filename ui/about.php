<p><h2 id="About">Welcome to LifeFLOW! :)</h2></p>

<p><a href="http://life.worldsowisdom.com/flow"><img src="./img/LifeFLOW.png" align="right"><b>LifeFLOW</b></a> is a fun evolving part of <a href="http://life.worldsowisdom.com/">Life Worlds O Wisdom (LifeWOW)</a>. It is kind of like a game, and kind of like nature! :)</p>

<p>You can think of LifeFLOW as a digital <a href="https://en.wikipedia.org/wiki/Vivarium">vivarium</a>, or <b><i>digitarium</i></b> (i.e. a <a href="https://life.worldsowisdom.com/?q=node/91">virtual terrarium/aquarium</a>). We create and play with living electronic creatures who travel their environmental realms. How cool is that? :)</p>

<p>A hylozoogical garden, a constructal instructal, a physical living receptacle between the mathematical and the biological, however you look at it LifeFLOW is a perspective-warping wonderworld! :)</p>

<p>LifeFLOW is a little place where we can play with moving machines. Each of these creatures goes around the area exploring and playing. You can observe them move, interact with them by touching, cloning, creating, removing, and more. New features continue to become available. You can also navigate around many different areas. <strong>Please contribute something to the flow, and enjoy! :)</strong></p>

<p>For extra special creatures, we store information about them at <b><a href="http://life.worldsowisdom.com/?q=bank">LifeBANK</a></b>. This includes data like name and code, and metadata like imagery and what makes each creature so special.</p>

<p>You can create a variety of creature types already, and we're continuing to add more. Try pressing some of the buttons! :)</p>

<p><b>A few tips:</b> Use the <font color="blue">blue square navigator</font> to browse around the areas. Use the <font color="green">green square browser</font> to navigate among the creatures. Currently the first few columns of creatures are unique, the rest are cool. Column six (creatures 60-69) contains psychedelic creatures. Currently areas save every full period. Columns four to six (areas 40-69) contain three different psychedelic backgrounds, one for each of those columns. We're still in the very early stages and welcome your comments on growth and development!</p>

<p><b>Some questions for consideration:</b> How would you describe LifeFLOW? How does a digitarium compare with a terrarium or aquarium? Do you consider LifeFLOW creatures "alive"? What does it mean to you for something to be alive? What does evolution have to do with living? What is life?

<p><b>Some questions for improvement:</b> What do you like or dislike about LifeFLOW? What do you want next? What do you think makes or would make LifeFLOW more useful, beautiful, interesting?</p>

<p><b>Development:</b> LifeFLOW is free and open source (MIT license). <a href="https://github.com/worldsowisdom/LifeFLOW">Come join us (at GitHub) in creating the coolest software on the planet! :)</a>

<p><b>Contact:</b> <a href="mailto:life@worldsowisdom.com?subject=LifeFLOW">life@worldsowisdom.com</a>! :)</p>


